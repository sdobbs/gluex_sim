//----------------------
// Init
//----------------------
int Init(double Ebeam)
{
  BEAM_ENERGY = Ebeam; // Remember beam energy for later use
  
  //---- Convert cross section table to integral fractions ----
  // First, numerically integrate
  cross_section_t *c=pi0_H_4_5GeV;
  for(int i=0;i<N_pi0_H_4_5GeV;i++,c++){
    It += c->total;	    
    c->total = It;
    Ip += c->primakoff;	    
    c->primakoff = Ip;
    In += c->nuclear;	    
    c->nuclear = In;
    Ii += c->interference;  
    c->interference = Ii;
  }
  // Next, normalize tables
  c=pi0_H_4_5GeV;
  for(int i=0;i<N_pi0_H_4_5GeV;i++,c++){
    c->theta /= 1000.0; //convert from mrad to rad for later
    c->total /= It;
    c->primakoff /= Ip;
    c->nuclear /= In;
    c->interference /= Ii;
  }
  
  // Normalize three total integrals to total so we can generate
  // them in the proper proportions
  double Itotal = Ip + In + Ii;
  Ip/=Itotal;
  In/=Itotal;
  Ii/=Itotal;
  
  return 0;
}

//----------------------
// GenerateEvent
//----------------------
int GenerateEvent(int *Ninitial	// Number of incident particles
		  ,mcparticle_t* initial // Array of incident particles
		  ,int *Nintermediary // Number of intermediary particles
		  ,mcparticle_t* intermediary // Intermediary particles array
		  ,int *Nfinal // Number of final state particles
		  ,mcparticle_t* final // Array of final state particles
		  ,int *production_mech	// prod channel (specific to generator)
		  ,int *decay_mech) // decay channel (specific to generator)
{
  // Incident photon
  *Ninitial = 1;
  initial->type = ptype_gamma;
  initial->v.E = BEAM_ENERGY;

  // sample vertex from a cylindrical target aligned with the beamline.
  // for simplicity, let the radial distribution be exponential.
  float beam_width = 0.2*cm;
  float target_radius = 1.0*cm;
  float target_length = 40.0*cm;
  float r;
  float k = log(2.0)/beam_width;
  do{
    double s = ((double)random()/(double)RAND_MAX);
    r = fabs(log(1.0-s)/k);
  }while(r>target_radius);
  double s = ((double)random()/(double)RAND_MAX);
  double ss = ((double)random()/(double)RAND_MAX);
  float vphi = 2.0*M_PI*s;
  float z = ss*target_length - (target_length/2.0);
  initial->x = r*cos(vphi);	
  initial->y = r*sin(vphi);	
  initial->z = z;
  initial->v.x = 0.0;
  initial->v.y = 0.0;			
  initial->v.z = initial->v.E;
  
  // Intermediary state particles (pi0)
  // -- Determine production mechanism
  s = ((double)random()/(double)RAND_MAX);
  ss = ((double)random()/(double)RAND_MAX);
  cross_section_t *c=pi0_H_4_5GeV;
  int index;
  double delta_cross;
  if(s<=Ip){
    // Primakoff
    for(index=0;index<N_pi0_H_4_5GeV-1;index++,c++)if(ss <= c->primakoff)break;
    delta_cross = c->primakoff + c[1].primakoff;
    *production_mech=0;
  }else if(s<=(Ip+In)){
    // Nuclear Coherent
    for(index=0;index<N_pi0_H_4_5GeV-1;index++,c++)if(ss <= c->nuclear)break;
    delta_cross = c->nuclear + c[1].nuclear;
    *production_mech=1;
  }else{
    // Interference
    for(index=0;index<N_pi0_H_4_5GeV-1;index++,c++)if(ss <= c->interference)break;
    delta_cross = c->interference + c[1].interference;
    *production_mech=2;
  }
  double pi0_theta=c->theta+(c->theta+c[1].theta)*(ss-c->theta)/delta_cross;
  double pi0_phi = 2.0*M_PI*((double)random()/(double)RAND_MAX);
  
  // Smear pi0 theta by +/-10^3 degrees to wash out some binning effects
  pi0_theta += (M_PI/180.0)*0.005*(1.0 - 2.0*((double)random()/(double)RAND_MAX));
  
  // Calculate pi0 momentum
  double M_N = PROTON_MASS;
  double Eg = initial->v.E;
  double L = Eg/(Eg + M_N);
  double K = (Eg*M_N + pow(PI_ZERO_MASS,2.0)/2.0)/(Eg + M_N);
  double A = pow(L*cos(pi0_theta),2.0) - 1.0;
  double B = 2.0*K*L*cos(pi0_theta);
  double C = pow(K,2.0) - pow(PI_ZERO_MASS,2.0);
  double pi0_p = ((-B) - sqrt(B*B - 4.0*A*C))/(2.0*A);
  //double pi0_p = sqrt(pow(initial->v.E,2.0) - pow(PI_ZERO_MASS,2.0));
  intermediary->v.E = sqrt(pow(pi0_p,2.0) + pow(PI_ZERO_MASS,2.0));
	     
  *Nintermediary = 1;
  intermediary->type = ptype_pion_zero;
  double vertex_r = 0.5*cm*(-0.5 + ((double)random()/(double)RAND_MAX));
  double vertex_phi = 2.0*M_PI*((double)random()/(double)RAND_MAX);
  intermediary->x = vertex_r*cos(vertex_phi);
  intermediary->y = vertex_r*sin(vertex_phi);
  intermediary->z = 65.0*cm + 30.0*cm*(-0.5 + ((double)random()/(double)RAND_MAX));
  
  intermediary->v.x = pi0_p*sin(pi0_theta)*cos(pi0_phi);
  intermediary->v.y = pi0_p*sin(pi0_theta)*sin(pi0_phi);
  intermediary->v.z = pi0_p*cos(pi0_theta);
  
  // ------------ Final state photons ---------------
  // Define decay photons in rest frame of pi0
  *Nfinal = 2;
  final[0].type = ptype_gamma;
  final[0].v.E = PI_ZERO_MASS/2.0;
  final[0].x = intermediary->x;
  final[0].y = intermediary->y;
  final[0].z = intermediary->z;
  
  double phi = 2.0*M_PI*((double)random()/(double)RAND_MAX);
  double theta = M_PI*((double)random()/(double)RAND_MAX);
  
  final[0].v.x = final[0].v.E*sin(theta)*cos(phi);
  final[0].v.y = final[0].v.E*sin(theta)*sin(phi);
  final[0].v.z = final[0].v.E*cos(theta);
  
  // second decay photon goes in opposite direction
  final[1] = final[0];
  final[1].v.x = -final[0].v.x;
  final[1].v.y = -final[0].v.y;
  final[1].v.z = -final[0].v.z;

  // Boost photons into lab frame (http://rd11.web.cern.ch/RD11/rkb/PH14pp/node105.html)
  for(int i=0;i<*Nfinal;i++){
    final[i].v.E = (final[i].v.E*intermediary->v.E
		    + final[i].v.x*intermediary->v.x
		    + final[i].v.y*intermediary->v.y
		    + final[i].v.z*intermediary->v.z)/PI_ZERO_MASS;
    final[i].v.x += intermediary->v.x*(PI_ZERO_MASS/2.0 + final[i].v.E)/(intermediary->v.E + PI_ZERO_MASS);
    final[i].v.y += intermediary->v.y*(PI_ZERO_MASS/2.0 + final[i].v.E)/(intermediary->v.E + PI_ZERO_MASS);
    final[i].v.z += intermediary->v.z*(PI_ZERO_MASS/2.0 + final[i].v.E)/(intermediary->v.E + PI_ZERO_MASS);
  }

  // Final state proton
  vect4 fpi0 = vect4_add(final[0].v, final[1].v);
  vect4 tgt = {PROTON_MASS, 0.0, 0.0, 0.0};
  vect4 iall = vect4_add(tgt, initial[0].v);
  final[2].v = vect4_sub(iall, fpi0);
  final[2].type = ptype_proton;
  final[2].x = final[0].x;
  final[2].y = final[0].y;
  final[2].z = final[0].z;
  (*Nfinal)++;
  
  // Only one decay mechanisim for this generator!
  *decay_mech=0;
  return 0;
}

